    private void enregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enregistrerActionPerformed

        if(dateDChooser.getDate() == null || dateDChooser.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Les dates de début ou fin ne peuvent être nulles!", "Ajouter Seance", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Date dateD = new Date(dateDChooser.getDate().getTime());
        Date dateF = new Date(dateFChooser.getDate().getTime());
        String nomprecombo = (String) ensCombo.getSelectedItem();
        String nommat = (String) matiereCombo.getSelectedItem();
        for(int i = 0; i < listens.size(); i++) {
            ens = (Enseignant) listens.get(i);
            if(nomprecombo.equals(ens.getNomEns() + " " + ens.getPrenomEns())) {
                break;
            }
        }
        
        for(int i = 0; i < listmat.size(); i++) {
            mat = (Matiere) listmat.get(i);
            if(nommat.equals(mat.getNomMatiere())) {
                break;
            }
        }
        
        Time t = this.getMillisTime(this.heureSeance);
        StatiqueCreneau sc = new StatiqueCreneau(mat.getNumMatiere(), this.numGroupe, ens.getNumEns(), jourSemaine, t, dateD, dateF);
        StatiqCrenoDAO scdao = new StatiqCrenoDAO(conn);
        scdao.create(sc);
        SeanceDAO s = new SeanceDAO(conn);
        s.createAllSeance(sc);
        this.gererEmploi.getEmp(numGroupe);
        this.dispose();
    }//GEN-LAST:event_enregistrerActionPerformed
    
    public Time getMillisTime(String h) {
        
        Time t;
        Calendar cal = Calendar.getInstance();
        if (h.equals("08:00:00")) {
            cal.set(0, 0, 0, 8, 0, 0);
        } else if(h.equals("10:00:00")){
            cal.set(0, 0, 0, 10, 0, 0);
        } else if(h.equals("14:00:00")){
            cal.set(0, 0, 0, 14, 0, 0);
        } else if(h.equals("16:00:00")){
            cal.set(0, 0, 0, 16, 0, 0);
        }
        
        else return null;
        long timeMillis = cal.getTimeInMillis();
        t = new Time(timeMillis);
        return t;
    }
    
